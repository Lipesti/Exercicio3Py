import math

graus = float(input('Informe os graus de um angulo qualquer: '))

seno = print('Seno {:.2f}'.format(math.sin(math.radians(graus))))
cos = print('Coseno {:.2f}'.format(math.cos(math.radians(graus))))
tan = print('Tangente {:.2f}'.format(math.tan(math.radians(graus))))